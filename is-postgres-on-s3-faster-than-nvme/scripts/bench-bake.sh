#!/usr/bin/env bash
# Bake an AMI from the currently-up EC2 instance.
#
# Workflow:
#   1. Run `ansible-playbook bake.yml` (or full `make up`) against the live
#      primary first — we assume that's been done.
#   2. Stamp /etc/pgbench/baked + clean transient scenario state.
#   3. `aws ec2 create-image --no-reboot`.
#   4. Wait until the AMI is `available`.
#   5. Write the AMI id into `terraform/ami.auto.tfvars` so subsequent
#      `make up` calls boot from it.

source "$(dirname "$0")/_lib.sh"

require_tf
require_ssm
require aws jq

INSTANCE="$(primary_instance_id)"
REGION="$(aws_region)"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
AMI_NAME="pgbench-base-${TIMESTAMP}"
BAKED_AT="$(date -Iseconds)"

# Build the cleanup script in a tmp file. Single-line `commands=[…]` payloads
# break on quoting easily; staging via S3 keeps the shell-escape surface tiny.
CLEANUP_SCRIPT="$(mktemp /tmp/bench-bake-cleanup.XXXXXX.sh)"
trap 'rm -f "$CLEANUP_SCRIPT"' EXIT

cat > "$CLEANUP_SCRIPT" <<'CLEANUP_EOF'
#!/usr/bin/env bash
# Cleanup script — run on the EC2 host before the AMI snapshot.
# Strips scenario-specific runtime state so the AMI is scenario-agnostic.
set -eu

# Stop & wipe Postgres cluster registration (recreated at runtime per scenario).
systemctl stop  postgresql@18-main 2>/dev/null || true
systemctl stop  postgresql.service 2>/dev/null || true
systemctl disable postgresql.service 2>/dev/null || true
pg_dropcluster --stop 18 main 2>/dev/null || true
rm -rf /etc/postgresql/18/main /var/lib/postgresql/18/main

# Unmount + remove fstab entries that reference scenario mounts. Use grep
# rewrite (not sed-with-alt-delimiter) so it's robust to quoting / escaping.
umount /mnt/pgdata 2>/dev/null || true
umount /mnt/zerofs-cache 2>/dev/null || true
umount /mnt/slatedb-cache 2>/dev/null || true
if [ -f /etc/fstab ]; then
  grep -vE '/mnt/(pgdata|zerofs-cache|slatedb-cache)' /etc/fstab > /etc/fstab.bake.tmp || true
  mv /etc/fstab.bake.tmp /etc/fstab
fi

# Drop scenario metadata + bench-run artifacts.
rm -f /opt/bench/scenario.json
rm -rf /opt/bench/runs/* 2>/dev/null || true

echo "cleanup.sh: done"
CLEANUP_EOF

# Stage cleanup script via S3 (small script, no shell-escape quoting concerns).
RESULTS_BUCKET="$(results_bucket)"
S3_KEY="_tmp/bench-bake-cleanup-${TIMESTAMP}.sh"
S3_URI="s3://$RESULTS_BUCKET/$S3_KEY"

info "==> Staging cleanup script via S3: $S3_URI"
aws s3 cp --region "$REGION" --quiet "$CLEANUP_SCRIPT" "$S3_URI"

# Step 1: write the sentinel as its own discrete SSM command (no script
# generation, just an inline mkdir + heredoc — easier to debug).
info "==> Writing AMI sentinel /etc/pgbench/baked on $INSTANCE"
ssm_run_on_primary "$(cat <<EOS
set -eu
mkdir -p /etc/pgbench
cat > /etc/pgbench/baked <<'META'
{"baked_at":"$BAKED_AT","ami_name":"$AMI_NAME"}
META
chmod 644 /etc/pgbench/baked
ls -la /etc/pgbench/baked
EOS
)"

# Step 2: run the (separately-staged, well-tested) cleanup script.
info "==> Cleaning transient state on $INSTANCE"
ssm_run_on_primary "set -eu
aws s3 cp --region $REGION '$S3_URI' /tmp/cleanup.sh
bash /tmp/cleanup.sh
rm -f /tmp/cleanup.sh
aws s3 rm --region $REGION '$S3_URI' >/dev/null
"

info "==> Snapshotting $INSTANCE → AMI $AMI_NAME"
AMI_ID=$(aws ec2 create-image \
    --region "$REGION" \
    --instance-id "$INSTANCE" \
    --name "$AMI_NAME" \
    --description "Postgres benchmark base AMI (apt + sysctl + PG18 binaries)" \
    --no-reboot \
    --tag-specifications 'ResourceType=image,Tags=[{Key=Project,Value=postgres-benchmark-s3-nvme},{Key=ManagedBy,Value=bench-bake.sh}]' \
    --output text --query ImageId)

info "    AMI id: $AMI_ID"
info "==> Waiting for AMI to become available (this can take 5-30 min)…"
# Manual poll loop instead of `aws ec2 wait image-available` — the latter
# defaults to 80×15s = 20 min, which the bigger snapshots sometimes exceed.
# We give it 60 minutes here and only then bail.
DEADLINE=$(( $(date +%s) + 3600 ))
while :; do
  STATE=$(aws ec2 describe-images --region "$REGION" --image-ids "$AMI_ID" \
      --query 'Images[0].State' --output text 2>/dev/null || echo unknown)
  case "$STATE" in
    available) info "    AMI is available."; break ;;
    pending)   printf '.'; sleep 20 ;;
    failed)    err "AMI build failed (state=failed)"; exit 1 ;;
    *)         printf '?'; sleep 20 ;;
  esac
  [ "$(date +%s)" -gt "$DEADLINE" ] && {
    err "Timed out after 60 min waiting for $AMI_ID. Check the AWS console; if it eventually goes available, write base_ami_id manually to terraform/ami.auto.tfvars."
    exit 1
  }
done
echo

# Persist the AMI id in terraform/ami.auto.tfvars (gitignored). Subsequent
# `terraform apply` will pick it up automatically via base_ami_id.
TFVARS="$TF_DIR/ami.auto.tfvars"
cat > "$TFVARS" <<EOF
# Generated by scripts/bench-bake.sh on $BAKED_AT
# Source instance: $INSTANCE
# AMI name:        $AMI_NAME
base_ami_id = "$AMI_ID"
EOF

info "==> Wrote $TFVARS"
info "==> Done. Next: 'make down' then 'make up SCENARIO=<...>' will boot from $AMI_ID"
